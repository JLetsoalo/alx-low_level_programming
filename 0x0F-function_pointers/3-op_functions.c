#include "3-calc.h"

/**
 * op_add - addition opperator
 * @a: integer 1
 * @b: integer 2
 * Return: sum of a & b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction of 2 integers
 * @a: first integer
 * @b: second integer
 * Return: results of a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplying
 * @a: integer one
 * @b: integer two
 * Return: results of a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - dividing a with b
 * @a: 1st integer to divide
 * @b: 2nd integer to divide
 * Return: results
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus opp for remainder
 * @a: integer 1
 * @b: integer 2
 * Return: function end output
 */

int op_mod(int a, int b)
{
	return (a % b);
}
